#include "libft.h"

int	ft_check(char c)
{
	if (c != 'i' && c != 'c' && c != 's' && c != 'x'
		&& c != 'X' && c != 'u' && c != 'd' && c != 'p')
	{
		write (1, &c, 1);
		return (1);
	}
	return (0);
}
